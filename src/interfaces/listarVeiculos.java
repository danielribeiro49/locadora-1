    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //exclui a porra toda
        
       try{
       int linha =  jTable1.getSelectedRow();
       String placa = (jTable1.getValueAt(linha, 3)).toString();   
       VeiculoDAO v = new VeiculoDAO(con);
       if (v.disponivel(placa)){
       
       
       int excluiu = v.delete(placa);
          
       if(excluiu == 1){
         JOptionPane.showMessageDialog(this, "Veículo excluído com sucesso.");
       }else {
         JOptionPane.showMessageDialog(this, "ERRO: Exclusão não efetuada.");
       }}else{
         JOptionPane.showMessageDialog(this, "ERRO: Exclusão não efetuada, o veiculo encontra-se locado!"); 
       }
     
     
       }catch(SQLException e){
            System.out.println("erro de sql");
       }catch(java.lang.ArrayIndexOutOfBoundsException e){
           JOptionPane.showMessageDialog(this, "Selecione um veículo.");
       }
       
       jTable1.repaint();
       atualizarTabela();
       
     
        
    }//GEN-LAST:event_jButton2ActionPerformed

