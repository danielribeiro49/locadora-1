     public void atualizarTabela(){
          try{
              clearTable();
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
             VeiculoDAO v = new VeiculoDAO(con); 
             LoginDAO lg = new LoginDAO(con);
             CategoriaDAO cDAO = new CategoriaDAO(con);
             
             ResultSet rs = v.retrive();
            while(rs.next()){
                String placa = rs.getString("placa");
                int fk_unidade = rs.getInt("fk_unidade");        
                String unidade = lg.retriveNome(fk_unidade);   
                String fk_categoria = rs.getString("fk_categoria");
                String cor = rs.getString("cor");
                int numportas = rs.getInt("numportas");
                int ano = rs.getInt("ano");
                String disponivel = rs.getString("disponivel");
                String modelo = rs.getString("modelo");
              
                model.addRow(new Object[]{modelo, cor, ano, placa, numportas, fk_categoria, disponivel, unidade});
            }               
       }catch(SQLException e){
            System.out.println("erro sql");
        }
    }
    
  
    
